const withOpacity = (variableName: string) => ({ opacityValue }: { opacityValue?: string }) => {
  if (opacityValue === undefined) {
    return `hsl(var(${variableName}))`;
  }

  return `hsla(var(${variableName}), ${opacityValue})`;
};

